/**
 * For radian conversion
 *
 * @constant
 * @type    {Number}
 */
const PI180 = Math.PI / 180

/**
 * Twice of PI
 *
 * @constant
 * @type {Number}
 */
const PI2 = Math.PI * 2

/**
 * Half of PI
 *
 * @constant
 * @type {Number}
 */
const HALFPI = Math.PI / 2

export { PI2, PI180, HALFPI }
